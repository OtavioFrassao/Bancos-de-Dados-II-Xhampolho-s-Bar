CREATE OR REPLACE PROCEDURE CADASTRAR_AREA(NOME IN VARCHAR2) IS
    v_num_areas NUMBER;
BEGIN
    SELECT NVL(MAX(ID_AREA), 0) INTO v_num_areas FROM AREA;
    INSERT INTO AREA(ID_AREA, NOME_AREA) VALUES(v_num_areas + 1, NOME);
END;
/

CREATE OR REPLACE PROCEDURE REMOVER_AREA_CODIGO(CODIGO IN NUMBER) IS
BEGIN
    DELETE FROM AREA WHERE ID_AREA = CODIGO;
END;
/

CREATE OR REPLACE PROCEDURE REMOVER_AREA_NOME(NOME IN VARCHAR2) IS
BEGIN
    DELETE FROM AREA WHERE NOME_AREA = NOME;
END;
/

CREATE OR REPLACE FUNCTION BUSCAR_AREA_CODIGO(CODIGO IN NUMBER) RETURN VARCHAR2 IS
    v_codigo_area VARCHAR2(255);
    v_nome_area VARCHAR2(255);
BEGIN
    SELECT ID_AREA, NOME_AREA
    INTO v_codigo_area, v_nome_area
    FROM AREA
    WHERE ID_AREA = CODIGO;
    
    RETURN 'Codigo(' || v_codigo_area || ') - Área: ' || v_nome_area;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RETURN 'Área com o código ' || CODIGO || ' não encontrada.';
END;
/

DECLARE
BEGIN
    FOR reg IN (SELECT ID_AREA FROM AREA ORDER BY ID_AREA) LOOP
        DBMS_OUTPUT.PUT_LINE(BUSCAR_AREA_CODIGO(reg.ID_AREA));
    END LOOP;
END;
/

CREATE OR REPLACE PROCEDURE ATUALIZAR_NOME_AREA(CODIGO IN NUMBER, NOVO_NOME IN VARCHAR2) IS
BEGIN
    UPDATE AREA SET NOME_AREA = NOVO_NOME WHERE ID_AREA = CODIGO;
END;
/


