CREATE OR REPLACE PROCEDURE ADICIONAR_PRODUTO_ESTOQUE(ID_PRODUTO_IN IN INTEGER, QUANTIDADE_IN IN INTEGER) AS
    V_QTD_ATUAL NUMBER := 0;
BEGIN
    SELECT QUANTIDADE INTO V_QTD_ATUAL FROM PRODUTO WHERE ID_PRODUTO = ID_PRODUTO_IN;
    
    V_QTD_ATUAL := V_QTD_ATUAL + QUANTIDADE_IN;
    
    UPDATE PRODUTO SET QUANTIDADE = V_QTD_ATUAL WHERE ID_PRODUTO = ID_PRODUTO_IN;
    DBMS_OUTPUT.PUT_LINE('Nova quantidade em estoque: ' || V_QTD_ATUAL);
END;
/

CREATE OR REPLACE PROCEDURE REMOVER_PRODUTO_ESTOQUE(ID_PRODUTO_IN IN INTEGER, QUANTIDADE_IN IN INTEGER) AS
    V_QTD_ATUAL NUMBER := 0;
BEGIN
    SELECT QUANTIDADE INTO V_QTD_ATUAL FROM PRODUTO WHERE ID_PRODUTO = ID_PRODUTO_IN;
    
    IF (V_QTD_ATUAL - QUANTIDADE_IN < 0) THEN
        RAISE_APPLICATION_ERROR(-20020, 'ESTOQUE INSUFICIENTE PARA REMOÇÃO. Atual: ' || V_QTD_ATUAL);
    END IF;

    V_QTD_ATUAL := V_QTD_ATUAL - QUANTIDADE_IN;
    
    UPDATE PRODUTO SET QUANTIDADE = V_QTD_ATUAL WHERE ID_PRODUTO = ID_PRODUTO_IN;
    DBMS_OUTPUT.PUT_LINE('Nova quantidade em estoque: ' || V_QTD_ATUAL);
END;
/

