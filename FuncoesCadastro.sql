CREATE OR REPLACE FUNCTION BUSCAR_AREA_CODIGO(CODIGO IN NUMBER) RETURN VARCHAR2 IS
    v_codigo_area VARCHAR2(255);
    v_nome_area VARCHAR2(255);
    v_ret VARCHAR2(255);
BEGIN
    SELECT A.ID_AREA, A.NOME_AREA
    INTO v_codigo_area, v_nome_area
    FROM AREA A
    WHERE CODIGO = A.ID_AREA;
    
    v_ret := 'Codigo(' || v_codigo_area || ') - Área: ' || v_nome_area;
    RETURN v_ret;
END;
/

DECLARE
    v_num_areas NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_num_areas FROM AREA;
    FOR x in 1..v_num_areas LOOP
        DBMS_OUTPUT.PUT_LINE(BUSCAR_AREA_CODIGO(x));
    END LOOP;
END;
/

CREATE OR REPLACE PROCEDURE ATUALIZAR_NOME_AREA(CODIGO IN NUMBER, NOVO_NOME IN VARCHAR2) IS
BEGIN
    UPDATE AREA A SET A.NOME_AREA = NOVO_NOME
    WHERE CODIGO = A.ID_AREA;
END;
/

