/* CRUD - ÁREA */
/***************/
SELECT * FROM AREA ORDER BY ID_AREA;

CREATE OR REPLACE PROCEDURE CADASTRAR_AREA(NOME IN VARCHAR2) IS
    v_num_areas NUMBER := -99;
BEGIN
    SELECT NVL(MAX(ID_AREA),0) INTO v_num_areas FROM AREA;
        
    INSERT INTO AREA(NOME_AREA, ID_AREA) VALUES(NOME, v_num_areas+1);
END;
/
 // TRIGGER (SE JA HOUVER UMA AREA COM ESSE NOME, RETORNAR ERRO)

