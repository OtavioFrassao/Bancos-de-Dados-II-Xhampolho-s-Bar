/******************/
/* CRUD - PRODUTO */
/******************/
CREATE OR REPLACE TRIGGER TRG_BIU_PRODUTO
BEFORE INSERT OR UPDATE ON PRODUTO
FOR EACH ROW
DECLARE
    v_count_area INTEGER := 0;
BEGIN
    IF :NEW.NOME_PRODUTO IS NULL THEN
        RAISE_APPLICATION_ERROR(-20007, 'NÃO É PERMITIDO O CADASTRO DE NOMES VAZIOS (PRODUTO)');
    END IF;

    IF :NEW.PRECO_PRODUTO < 0 THEN
        RAISE_APPLICATION_ERROR(-20008, 'NÃO É PERMITIDO O CADASTRO DE VALORES MENORES QUE ZERO (PRODUTO - PRECO)');
    END IF;

    IF :NEW.QUANTIDADE < 0 THEN
        RAISE_APPLICATION_ERROR(-20009, 'NÃO É PERMITIDO O CADASTRO DE VALORES MENORES QUE ZERO (PRODUTO - QUANTIDADE)');
    END IF;

    IF :NEW.OBSERVACAO_PRODUTO IS NULL THEN
        RAISE_APPLICATION_ERROR(-20013, 'A OBSERVAÇÃO DO PRODUTO NÃO PODE SER VAZIA');
    END IF;

    IF :NEW.ID_AREA IS NOT NULL THEN
        SELECT COUNT(*) INTO v_count_area FROM AREA WHERE ID_AREA = :NEW.ID_AREA;
        IF v_count_area = 0 THEN
            RAISE_APPLICATION_ERROR(-20010, 'NÃO FOI ENCONTRADA NENHUMA ÁREA COM ESSE ID (' || :NEW.ID_AREA || ')');
        END IF;
    END IF;
END;
