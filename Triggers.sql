CREATE OR REPLACE TRIGGER TRG_BIU_AREA
BEFORE INSERT OR UPDATE ON AREA
FOR EACH ROW
BEGIN
    IF :NEW.NOME_AREA IS NULL THEN
        RAISE_APPLICATION_ERROR(-20001, 'NÃO É PERMITIDO O CADASTRO DE NOMES VAZIOS (AREAS)');
    END IF;
