/******************/
/* CRUD - COMANDA */
/******************/
CREATE OR REPLACE TRIGGER TRG_BIU_COMANDA
BEFORE INSERT OR UPDATE ON COMANDA
FOR EACH ROW
DECLARE
    v_status_mesa NUMBER;
BEGIN
    IF :NEW.ID_MESA IS NOT NULL THEN
        BEGIN
            SELECT DISPONIVEL INTO v_status_mesa 
            FROM MESA 
            WHERE ID_MESA = :NEW.ID_MESA;
            
        EXCEPTION
            WHEN NO_DATA_FOUND THEN
                RAISE_APPLICATION_ERROR(-20003, 'NÃO FOI ENCONTRADA UMA MESA COM ESSE ID.');
        END;
    END IF;
END;
