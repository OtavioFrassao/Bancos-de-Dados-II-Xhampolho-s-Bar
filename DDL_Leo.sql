DROP TABLE PEDIDO CASCADE CONSTRAINTS;
DROP TABLE PRODUTO CASCADE CONSTRAINTS;
DROP TABLE COMANDA CASCADE CONSTRAINTS;
DROP TABLE MESA CASCADE CONSTRAINTS;
DROP TABLE CLIENTE CASCADE CONSTRAINTS;
DROP TABLE FUNCIONARIO CASCADE CONSTRAINTS;
DROP TABLE AREA CASCADE CONSTRAINTS;
DROP TABLE USUARIO CASCADE CONSTRAINTS;

CREATE TABLE USUARIO(
ID_USUARIO INTEGER GENERATED BY DEFAULT AS IDENTITY,
NOME_USUARIO VARCHAR2(255),
CARGO_USUARIO VARCHAR2(255),
CONSTRAINT PK_ID_USUARIO PRIMARY KEY(ID_USUARIO)
);

CREATE TABLE CLIENTE(
    ID_CLIENTE INTEGER GENERATED BY DEFAULT AS IDENTITY,
    ID_USUARIO INTEGER,
    CONSTRAINT FK_CLIENTE_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO,
    CONSTRAINT PK_ID_CLIENTE PRIMARY KEY(ID_CLIENTE),
    NOME_CLIENTE VARCHAR2(255)
);

CREATE TABLE MESA(
    ID_MESA INTEGER GENERATED BY DEFAULT AS IDENTITY,
    DISPONIVEL NUMBER(1) DEFAULT 1 NOT NULL,
    CONSTRAINT PK_ID_MESA PRIMARY KEY(ID_MESA),
    CONSTRAINT CK_MESA_DISPONIVEL CHECK(DISPONIVEL IN (0,1))
);

CREATE TABLE AREA(
    ID_AREA INTEGER GENERATED BY DEFAULT AS IDENTITY,
    NOME_AREA VARCHAR2(255),
    CONSTRAINT PK_ID_AREA PRIMARY KEY(ID_AREA)
);

CREATE TABLE COMANDA(
    ID_COMANDA INTEGER GENERATED BY DEFAULT AS IDENTITY,
    ID_MESA INTEGER,
    NOME_CLIENTE VARCHAR2(255),
    PRECO_TOTAL NUMBER,
    CONSTRAINT FK_COMANDA_MESA FOREIGN KEY (ID_MESA) REFERENCES MESA,
    CONSTRAINT PK_ID_COMANDA PRIMARY KEY (ID_COMANDA)
);

CREATE TABLE FUNCIONARIO(   
    ID_FUNCIONARIO NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_USUARIO NUMBER NOT NULL,
    NOME VARCHAR(255),
    DATA_ADMISSAO DATE, 
    SALARIO NUMBER(10,2),
    DATA_NASCIMENTO DATE,
    CARGO VARCHAR2(255),
    CONSTRAINT PK_FUNCIONARIO PRIMARY KEY (ID_FUNCIONARIO),
    CONSTRAINT FK_FUNCIONARIO_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO),
    CONSTRAINT UK_FUNCIONARIO_USUARIO UNIQUE (ID_USUARIO));
   
   CREATE TABLE PRODUTO(
        ID_PRODUTO NUMBER GENERATED BY DEFAULT AS IDENTITY,
        NOME VARCHAR(255) NOT NULL,
        PRECO_PRODUTO NUMBER (10,2) NOT NULL,
        OBSERVACAO VARCHAR(255),
        ID_AREA NUMBER,
        QUANTIDADE INTEGER,
        DISPONIVEL NUMBER(1) DEFAULT 1, --1 PARA SIM, 0 PARA NAO
        CONSTRAINT PK_PRODUTO PRIMARY KEY (ID_PRODUTO),
        CONSTRAINT FK_PRODUTO_AREA FOREIGN KEY (ID_AREA) REFERENCES AREA(ID_AREA),
        CONSTRAINT CHK_PRODUTO_DISPONIVEL CHECK (DISPONIVEL IN (0,1))
        );
   
   CREATE TABLE PEDIDO(
        ID_PEDIDO NUMBER GENERATED BY DEFAULT AS IDENTITY,
        ID_COMANDA NUMBER NOT NULL,
        DATA_HORA DATE DEFAULT SYSDATE,
        STATUS VARCHAR(255),
        CONSTRAINT PK_PEDIDO PRIMARY KEY (ID_PEDIDO),
        CONSTRAINT FK_PEDIDO_COMANDA FOREIGN KEY (ID_COMANDA) REFERENCES COMANDA(ID_COMANDA)
        );
        
    
    CREATE TABLE PRODUTO_PEDIDO(
        ID_PRODUTO NUMBER NOT NULL,
        ID_PEDIDO NUMBER NOT NULL,
        QTD_PRODUTO NUMBER NOT NULL,
        PRECO_UNI NUMBER(10,2) NOT NULL,
        CONSTRAINT PK_PRODUTO_PEDIDO PRIMARY KEY (ID_PRODUTO, ID_PEDIDO),
        CONSTRAINT FK_PROD_PED_PRODUTO FOREIGN KEY (ID_PRODUTO) REFERENCES PRODUTO(ID_PRODUTO),
        CONSTRAINT FK_PROD_PED_PEDIDO FOREIGN KEY (ID_PEDIDO) REFERENCES PEDIDO(ID_PEDIDO)
        );
        
