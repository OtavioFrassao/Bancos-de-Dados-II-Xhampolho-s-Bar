CREATE TABLE USUARIO(
ID_USUARIO INTEGER GENERATED BY DEFAULT AS IDENTITY,
NOME_USUARIO VARCHAR2(255),
CARGO_USUARIO VARCHAR2(255),
CONSTRAINT PK_ID_USUARIO PRIMARY KEY(ID_USUARIO)
);

CREATE TABLE CLIENTE(
    ID_CLIENTE INTEGER GENERATED BY DEFAULT AS IDENTITY,
    ID_USUARIO INTEGER,
    CONSTRAINT FK_CLIENTE_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO,
    CONSTRAINT PK_ID_CLIENTE PRIMARY KEY(ID_CLIENTE),
    NOME_CLIENTE VARCHAR2(255)
);

CREATE TABLE MESA(
    ID_MESA INTEGER GENERATED BY DEFAULT AS IDENTITY,
    DISPONIVEL NUMBER(1) DEFAULT 1 NOT NULL,
    CONSTRAINT PK_ID_MESA PRIMARY KEY(ID_MESA),
    CONSTRAINT CK_MESA_DISPONIVEL CHECK(DISPONIVEL IN (0,1))
);

CREATE TABLE AREA(
    ID_AREA INTEGER GENERATED BY DEFAULT AS IDENTITY,
    NOME_AREA VARCHAR2(255),
    CONSTRAINT PK_ID_AREA PRIMARY KEY(ID_AREA)
);

CREATE TABLE COMANDA(
    ID_COMANDA INTEGER GENERATED BY DEFAULT AS IDENTITY,
    ID_MESA INTEGER,
    NOME_CLIENTE VARCHAR2(255),
    PRECO_TOTAL NUMBER,
    CONSTRAINT FK_COMANDA_MESA FOREIGN KEY (ID_MESA) REFERENCES MESA,
    CONSTRAINT PK_ID_COMANDA PRIMARY KEY (ID_COMANDA)
);

